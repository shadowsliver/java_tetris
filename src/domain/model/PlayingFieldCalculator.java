package domain.model;

public class PlayingFieldCalculator {
	
	private int height, width;
	
	public PlayingFieldCalculator(){
		height = 800;
		width = 400;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}
	
	public int getRows(){
		return 20;
	}
	
	public int getColums(){
		return 10;
	}
	
	public int getWidthColum(){
		return this.width/10;
	}
	
	public int getHeightRow(){
		return this.width*2/20;
	}
	

}
